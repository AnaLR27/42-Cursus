#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	d;

	d = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
		ft_putnbr_fd(n, fd);
	}
	else if (n >= 0 && n < 10)
	{
		d = n + 48;
		write(fd, &d, 1);
	}
}
